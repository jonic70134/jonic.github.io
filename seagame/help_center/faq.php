			<h5>發票問題</h5>
			<ol class="column black">
				<li>何時收到發票？
					<p>本公司於出貨後主動開出電子發票，不另寄送紙本發票，若需要看發票內容，請至 我的帳戶 > 查訂單 ，點選「發票」查看。</p>
				</li>
				<li>如何索取紙本發票？
					<p>請於訂購完成頁面點選「索取紙本發票」索取，您索取的紙本發票將會隨貨寄送。</p>
					<img src="../c/img/help_center/img_help050_adi01.gif">
				</li>
				<li>未索取紙本發票可以申請補開嗎？
					<p>若您訂購時未索取紙本發票，目前無法申請補開，請您諒解。</p>
				</li>
				<li>能否指定發票內容呢？
					<p>本公司發票為系統化自動開立，預設為您訂購的商品名稱和型號，不支援修改發票內容，給您帶來不便，請您諒解。</p>
				</li>
				<li>一筆訂單可以開立多張發票嗎？
					<p>每一筆訂單僅能開立一張發票，無法申請開立多張發票；若您有多筆發票需求，請分多筆訂單訂購。</p>
				</li>
				<li>發票打統編
					<p>發票需要打統編，請於填寫收貨地址時，點選「發票需打統編請按此」。</p>
				</li>
