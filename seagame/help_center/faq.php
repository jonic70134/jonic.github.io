			<h5>配送問題</h5>
			<ol class="column black">
				<li>有哪些配送方式可以選擇？
					<p>目前提供「宅配送達」與「至超商取貨」兩種方式。</p>
				</li>
				<li>如何修改收貨地址？
					<p>訂單出貨前，可至 <a href="https://member.uitox.com/account/home/5/AJ4023/1" target="_blank" class="hc_member_account">我的帳戶</a> > <a href="https://member.uitox.com/account/store/AJ4023/1" class="hc_check_cart_list" target="_blank">查訂單</a> ，點選「訂單明細」修改收貨地址；訂單已出貨，請點選「聯絡客服」留言，由客服人員替您處理。</p>
				</li>
				<li>如何查询出貨與配送進度？
					<p>您可於訂單出貨後，至 <a href="https://member.uitox.com/account/home/5/AJ4023/1" target="_blank" class="hc_member_account">我的帳戶</a> > <a href="https://member.uitox.com/account/store/AJ4023/1" class="hc_check_cart_list" target="_blank">查訂單</a> ，點選「出貨進度」的「詳」，查看商品配送進度。</p>
				</li>
				<li>可以指定配送時間嗎？
					<p>訂單出貨後，目前依物流分派與路順安排為主，目前暫不提供指定配送時間的服務，請您諒解。</p>
				</li>
				<li>簽收前，包裹明顯擠壓變形時可以拒收嗎？
					<p>簽收包裹前，請先檢查以下內容：收貨人姓名是否是您本人、包裹外包裝是否有明顯擠壓變形、破損狀況；
若發現異常，請您可拒收整個包裹，並聯繫客服。</p>
				</li>
				<li>簽收後，發現商品送錯/數量不符/瑕疵怎麼辦？
					<p>簽收包裹後，請至 <a href="https://member.uitox.com/account/home/5/AJ4023/1" target="_blank" class="hc_member_account">我的帳戶</a> > <a href="https://member.uitox.com/account/store/AJ4023/1" class="hc_check_cart_list" target="_blank">查訂單</a> ，點選「聯絡客服」留言，客服將與您聯繫、進行處理。</p>
				</li>
			</ol>
