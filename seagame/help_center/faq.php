			<ol class="column black">
				<li>我收到的商品不喜歡可以退貨嗎？
					<ul class="s-colum-2 gray2">
						<li>全站購物皆可享有商品到貨 七天猶豫期 之權益，如需退貨請至 <a href="https://member.uitox.com/account/home/5/AJ4023/1" target="_blank" class="hc_member_account">我的帳戶</a> > <a href="https://member.uitox.com/account/store/AJ4023/1" class="hc_check_cart_list" target="_blank">查訂單</a> 申請。</li>
