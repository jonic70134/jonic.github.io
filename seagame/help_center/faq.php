			<h5>退款問題<a name="h0505"></a></h5>
			<ol class="column black">
				<li>商品退回後多久可以收到退回款項？
					<ul class="s-colum-2 gray2">
						<li>申請退訂後，我們將於1-2個工作天派快遞前往取件；取回商品驗收無誤後，將於2至3個工作天內向銀行提出退款/取消授權申請，
		  銀行約需3到5個工作天作業。</li>
						<li>退款時，會有E-mail通知，您亦可於 <a href="https://member.uitox.com/account/home/5/AJ4023/1" target="_blank" class="hc_member_account">我的帳戶</a> > <a href="https://member.uitox.com/account/store/AJ4023/1" class="hc_check_cart_list" target="_blank">查訂單</a> ，查詢退款進度。</li>
						<li>若退回商品驗收上有任何問題，退款作業將暫停，客服人員將主動與您聯繫。</li>
					</ul>
				</li>
				<li>未出貨前退訂，多久可收到退款？
					<p>若您於出貨前辦理退訂，我們將於2至3個工作天內向銀行提出退款/取消授權申請，銀行約需3到5個工作天作業。</p>
